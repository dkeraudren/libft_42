#include "ft_lib.h"

int	ft_lstsize(t_list *lst)
{
	int		cnt;
	t_list	*current;

	cnt = 0;
	current = lst;
	while (current)
	{
		current = current->next;
		cnt++;
	}
	return (cnt);
}
